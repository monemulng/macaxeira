package com.macaxeira.model;

public class Ingrediente {
	private String nome;
	private int cod;
	
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public int getCod() {
		return cod;
	}

	public void setCod(int cod) {
		this.cod = cod;
	}

	public String toString() {
		return nome;
	}
}
